def hola_concatena():

    print('¡¡Vamos a concatenar palabras !!')

    palabra1 = str(input('Introduzca su primera palabra: '))
    palabra2 = str(input('Introduzca su segunda palabra: '))

    resultado = (palabra1 + ' ' +palabra2)

    print('Resultado concatenado: ', resultado)

hola_concatena()